# ─── Policy Agent ────────────────────────────────────────────────────────────

@trace_agent
def policy_agent_node(state: Dict[str, Any]) -> Dict[str, Any]:
    """Handles policy details, coverage, and auto policy information."""
    prompt = POLICY_AGENT_PROMPT.format(
        task=state.get("task", ""),
        policy_number=state.get("policy_number", "Not provided"),
        customer_id=state.get("customer_id", "Not provided"),
        conversation_history=state.get("conversation_history", ""),
    )
    tools = [
        {"type": "function", "function": {
            "name": "get_policy_details",
            "description": "Fetch policy info by policy number",
            "parameters": {"type": "object", "properties": {"policy_number": {"type": "string"}}},
        }},
        {"type": "function", "function": {
            "name": "get_auto_policy_details",
            "description": "Get auto policy details including vehicle info",
            "parameters": {"type": "object", "properties": {"policy_number": {"type": "string"}}},
        }},
    ]


    raw_result = run_llm(prompt, tools, {"get_policy_details": get_policy_details, "get_auto_policy_details": get_auto_policy_details})
    result = clean_llm_output(raw_result)
    return {
        "messages": [*state.get("messages", []), {"role": "assistant", "content": result}],
        "conversation_history": state.get("conversation_history", "") + f"\nPolicy Agent: {result}",
        # "end_conversation": True
    }


