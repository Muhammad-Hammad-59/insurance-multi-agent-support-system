def clean_llm_output(text: str) -> str:
    """Removes <think> tags generated by reasoning models to prevent context bleed."""
    if not isinstance(text, str):
        return text
    return re.sub(r'<think>.*?</think>', '', text, flags=re.DOTALL).strip()

