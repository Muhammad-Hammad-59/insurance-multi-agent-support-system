# ─── General Help Agent ──────────────────────────────────────────────────────

@trace_agent
def general_help_agent_node(state: Dict[str, Any]) -> Dict[str, Any]:
    """Answers general insurance FAQs using ChromaDB retrieval (RAG)."""
    user_query = state.get("user_input", "")
    faqs = search_faqs(user_query, n_results=3)

    faq_context = ""
    if faqs:
        for i, faq in enumerate(faqs):
            faq_context += f"FAQ {i+1} (score: {faq['score']:.3f})\nQ: {faq['question']}\nA: {faq['answer']}\n\n"
    else:
        faq_context = "No relevant FAQs were found."

    prompt = GENERAL_HELP_PROMPT.format(
        task=state.get("task", "General insurance support"),
        conversation_history=state.get("conversation_history", ""),
        faq_context=faq_context,
    )
    raw_result = run_llm(prompt)
    result = clean_llm_output(raw_result)
    return {
        "messages": [*state.get("messages", []), {"role": "assistant", "content": result}],
        "conversation_history": state.get("conversation_history", "") + f"\nGeneral Help Agent: {result}",
        # "end_conversation": True
    }


